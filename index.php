    <p>
        Les jeux de cartes à collectionner, ou JCC, utilisent des cartes conçues spécialement pour les jeux en question.
    Ils diffèrent des jeux de cartes classiques par la grande variété de cartes éditées et l'utilisation par les joueurs de paquets personnalisés.
    Les cartes ont en général des effets très variés sur le jeu.
    Une carte contient souvent un dessin de grande qualité (qui fait partie de l'intérêt du produit ; certains artistes doivent une part de leur notoriété à leurs travaux sur ces jeux) et un texte décrivant l'effet de la carte.
    Le jeu se déroule souvent dans un monde cohérent, de fantasy, de science-fiction ou autre ; il s'agit parfois d'univers déjà connus, par exemple dans des romans.
</p>
    <p>
    La plupart du temps, le jeu consiste à gagner des points ou à en faire perdre à l'adversaire : point de vie, d'énergie, d'honneur, de pouvoir.
    On utilise pour cela des cartes de ressource (qui permettent d'effectuer des actions), des cartes d'action (par exemple lancer une attaque sur l'adversaire), des cartes de contre (protection contre les attaques) et des cartes de cumul (qui permettent de mettre en réserve des ressources ou actions).
    Certaines cartes s'associent et permettent de faire des combinaisons (appelées aussi combos). Le mécanisme de ces jeux peut faire penser au 1000 bornes.
    Le premier jeu de ce type fut Magic : l'assemblée (Magic: the Gathering), édité par Wizards of the Coast à partir de 1993.
    Dans la plupart des cas, le jeu se déroule en piochant des cartes et en jouant au tour par tour.
</p>
    <p>
        Pour chaque jeu, il existe parfois des milliers de cartes : certains jeux proposent en effet régulièrement de nouvelles cartes qui complètent le jeu de base.
    Chaque joueur se constitue un jeu (deck) à partir des cartes dont il dispose.
    C'est la principale originalité des JCC : chaque deck est unique, conçu par le joueur dans une optique de jeu particulière.
    La phase de conception des decks, qui précède les parties elles-mêmes, est une part très importante de la stratégie et du plaisir de ce type de jeu.
    C'est là qu'intervient le « metagame », c’est-à-dire la prise en compte dans la conception du deck du jeu possible de l'adversaire.
    </p>
