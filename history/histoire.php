    <span>Gul'dan</span>, membre du <span>clan Ombrelune</span>, commença très tôt à apprendre à manipuler les éléments en compagnie de <span>Ner'zhul</span>, chef du clan. <span>Ner'zhul</span>, sous les ordres de <span>Kil'jaeden</span>, l'un des plus puissants démons de la <span>Légion Ardente</span>, et avec l'aide de <span>Gul'dan</span>, tentait de rallier les Orcs des différents clans de <span>Draenor</span> pour exterminer les Draeneï.
    Cependant, <span>Ner'Zhul</span> découvrit qu'il fut manipulé par la <span>Légion</span> et décida de revenir sur ses pas.
    Hélas pour lui, <span>Gul'dan</span> vit tout et retrouva <span>Kil'jaeden</span>, qui proposa à ce dernier de devenir un maître de la puissance démoniaque s'il unissait les orcs sous sa bannière.
    <span>Gul'dan</span> accepta, et corrompu des orcs pour qu'ils deviennent une <span>Horde sanglante</span>.
    <br /><br />
    Toujours sous le service de <span>Kil'jaeden</span>, <span>Gul'dan</span> créa le <span>Conseil des Ombres</span>, un groupe de Démoniste très puissant, ainsi que le clan des <span>Tisses-foudres</span>.
    Et alors que <span>Gul'dan</span> continuait d'augmenter son pouvoir, il corrompit les chefs de guerre des autres clans Orcs en leurs donnant à boire le sang du démon <span>Mannoroth</span>.
    Le résultat fut cataclysmique : la quasi-totalité des Orcs se transformèrent en horribles monstres à la peau verte et aux yeux rouges, massacrant les Draeneï de <span>Draenor</span>.
    Ce fut un véritable <span>génocide</span>.
    <br /><br />
    Quelque temps après, <span>Gul'dan</span> entendit l'appel du sorcier <span>Medivh</span>, corrompu par le chef de la <span>Légion Ardente</span>, le <span>Titan Noir Sargeras</span>.
    Il lui dit que dans la <span>Tombe de Sargeras</span> se trouvait une puissance incommensurable, qui lui offrirait un pouvoir titanesque et que le seul moyen d'y accéder serait d'envahir <span>Azeroth</span> avec sa <span>Horde</span>.
    Bien entendu, <span>Gul'dan</span> accepta et, avec l'aide de <span>Medivh</span>, ouvrit la <span>Porte des ténèbres</span> et fit déferler la <span>Horde</span> en <span>Azeroth</span>.
    <br /><br />
    Il s'ensuivit quelques années où <span>Gul'dan</span> manipula la <span>Horde</span> dans l'ombre avec son apprenti, <span>Cho'gall</span>, un ogre sorcier, s'arrangant pour que le <span>Chef de guerre des Rochenoires, Main-Noire</span>, père de <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/605">Rend</a>
    <img class="zecha-popover-image" src="http://www.hearthstone-decks.com/upload/carte/605.jpg"></span> et <span>Maim Main-Noire</span>, devint le <span>Chef de guerre de la Horde</span>.
    Mais alors que <span>Gul'dan</span> faisait tomber les royaumes humains, il apprit qu'un groupe d'humains se dirigeait vers la tour de <span>Karazhan</span> pour tuer <span>Medivh</span>.
    Il décida alors de chercher dans les pensées du dernier gardien l'emplacement de la <span>Tombe de Sargeras</span>.
    Cependant, <span>Medivh</span> fut tué et <span>Gul'dan</span>, encore dans ses pensées à ce moment, tomba dans un long coma...
    <br /><br />
    Quand <span>Gul'dan</span> sortit de son coma, il apprit la mort de <span>Main-Noire</span> et la chute du <span>Conseil des Ombres</span>.
    Il ne lui restait plus qu'un petit groupe de fidèles, dont son apprenti, <span>Cho'gall</span>.
    Il dut alors prouver sa valeur auprès du nouveau <span>Chef de guerre de la Horde</span>, <span>Orgrim Marteau-du-destin</span>, pour pouvoir survivre.
    Il créa les premiers <span>Chevaliers de la mort</span> pour servir la <span>Horde</span>, mais qui en fait le servaient lui.
    <br /><br />
    Cependant la soif de pouvoir du Démoniste finit par le conduire à sa fin.
    Il trahit la <span>Horde</span> et alla à la <span>Tombe de Sargeras</span>, enfouie au milieu des <span>Mers du Sud</span>, près du <span>Maelström</span>.
    Grâce à ses immenses pouvoirs, il remonta la Tombe à la surface et y entra.
    Mais, hélas pour lui, il comprit trop tard qu'il avait été en fait piégé par <span>Sargeras</span> : à l'intérieur du Tombeau se trouvaient des démons enragés qui le massacrèrent, ainsi que ses fidèles.
    C'est ainsi que <span>Gul'dan</span> mourut, trahi.
    <br /><br />
    Aujourd'hui, on peut encore voir en <span>Outreterre</span> un souvenir de <span>Gul'dan</span>, élevant une <span>montagne gangrenée</span> pour rompre le lien entre les Orcs et les éléments.
