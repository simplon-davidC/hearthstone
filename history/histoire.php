<br /><br />
    <span>Malfurion Hurlorage</span> est le frère jumeau d'<span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/55">Illidan Hurlorage</a>
    <img class="zecha-popover-image" src="http://www.hearthstone-decks.com/upload/carte/55.jpg"></span>.
    Ils grandirent tous deux avec <span>Tyrande Murmevent</span>, avant la <span>Grande Fracture</span>.
    Ils apprirent l'existence du demi-dieu <span class="over"><a class="rarete5" href="http://www:hearthstone-decks.com/carte/voir/47">Cénarius</a><img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/47.jpg"></span> et <span>Malfurion</span> devint son élève et le premier Druide.
    Un jour, le jeune Druide eut une vision effroyable qu'il fit par à son mentor.
    Le demi-dieu apprit que les Elfes de la nuit ( Kaldorei ) utilisaient trop abusivement l'énergie du <span>Puits d'éternité</span>.
    Cette utilisation abusive de la magie provoqua l'arrivée des démons de la Légion Ardente en <span>Azeroth</span>. C'était la <span>Guerre des Anciens</span>.<br /><br />
    <span>Malfurion</span>, <span>Tyrande</span> et <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/55">Illidan</a><img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/55.jpg"></span> combattirent vaillament les Démons.
    Cependant, le frère de Malfurion décida de quitter les Elfes pour rejoindre la <span>Légion</span>.
    Il devint alors un agent double. Pendant ce temps, <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/47">Cénarius</a>
    <img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/47.jpg"></span> continuait à enseigner à <span>Malfurion</span> tout ce qu'il savait sur le druidisme.
    Puis, il partit chercher l'aide des autres demi-dieux.
    Les Elfes s'allièrent avec les demis-dieux ( dont <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/47">Cénarius</a><img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/47.jpg"></span> et <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/589">Malorne</a>
    <img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/589.jpg"></span> ) ainsi que le <span>vol draconique vert</span> d'<span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/75">Ysera</a><img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/75.jpg"></span>.
    <span>Malfurion</span> referma le portail qui permettait aux Démons de venir en <span>Azeroth</span> : le <span>Puit d'éternité</span>.
    Cela empêcha l'arrivé de <span>Sargeras</span>, le chef de la <span>Légion Ardente</span>.<br /><br />
    Cependant, la surcharge provoquée par la fermeture fit imploser le <span>Puits</span>, ce qui déclancha le <span>Grande Fracture</span>.
    L'unique continent qu'était <span>Kalimdor</span> fut séparé en plusieurs autres continant et le <span>Maelström</span> apparut à la place du <span>Puits</span>.
    L'eau glaciale de la nouvelle mer chassa les Elfes et leurs alliés jusqu'au <span>Mont Hyjal</span>.
    Là-bas, ils purent se reposer et soigner leurs blessures.
    <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/55">Illidan</a><img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/55.jpg"></span>, toujours au service de la <span>Légion</span>, découvrit un lac qu'il voulut transformer en <span>Puits d'éternité</span>.
    Lorsque des Elfes l'aperçurent, il les attaqua. Heureusement, <span>Malfurion</span> arriva peu de temps après et emprisonna son frère.
    Avec <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/47">Cénarius</a><img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/47.jpg"></span>, il décida de l'enfermer dans une prison souterraine jusqu'à la fin des temps.<br /><br />
    Les dragons verts apprirent qu'un second <span>Puits</span> avait été créé et décidèrent de faire pousser un arbre immense au-dessus afin de le protéger de tout abus de magie. <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/75">Ysera</a><img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/75.jpg"></span>, chef du vol draconique vert, lia l'<span>Arbre-Monde Nordrassil</span> au <span>Rêve d'Émeraude</span>, une copie du monde vierge de la guerre. Ce lien permit aux Druides d'accéder et d'explorer le royaume du <span>Rêve</span>.
    <span>Malfurion</span>, devenu le compagnon de la grande prêtresse <span>Tyrande Murmevent</span>, fut l'un des premiers à y accéder. Il y resta endormit pendant de longues années...<br /><br />
    Un jour, il fut réveillé par <span>Tyrande</span>, qui avait besoin de lui pour repousser la <span>Légion Ardente</span>, de retour en <span>Azeroth</span>.
    Il combattit alors aux côtés de son âme-soeur ainsi que de <span>Thrall</span> et de <span>Jaina Portvaillant</span>.
    Lorsqu'il voulut retourner à son sommeil, il découvrit qu'un Satyre du nom de <span>Xavius</span> essayait de corrompre le <span>Rêve</span>.
    Il le repoussa et nul ne sut ce qu'il advint de <span>Xavius</span>.<br /><br />
    Pendant le <span>Cataclysme</span>, <span>Malfurion</span> décida de monter une armée pour vaincre <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/67">Ragnaros, le seigneur du feu</a><img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/67.jpg"></span>, qui tentait de brûler l'<span>Arbre-Monde</span>.
    Lorsque le <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/67">seigneur du feu</a><img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/67.jpg"></span> et <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/39">Aile de mort</a><img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/39.jpg"></span> fut vaincu, <span>Malfurion</span> rejoignit <span>Tyrande</span> pour l'aider à gouverner les Elfes de la nuit. Avec le futur retour de la <span>Légion</span> en <span>Azeroth</span>, Xavius tentera une nouvelle fois de corrompre le <span>Rêve</span>.<br /><br />
