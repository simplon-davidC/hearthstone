    <br /><br />
    <span>Medivh</span>, fils de l'archimage <span>Aegwynn</span> et du conjurateur <span>Nielas Aran</span>, fut le dernier <span>Gardien de Tirisfal</span>.
    <span>L'ordre de Tirisfal</span> avait pour de but de protéger le monde contre les démons de la <span>Légion Ardente</span>.
    Il en faisait partie notamment son père et sa mère.
    Lorsque sa mère, <span>Aegwynn Magna</span>, combattit <span>Sargeras</span>, le chef suprême de la <span>Légion Ardente</span>, l'esprit du <span>Titan Noir</span> s'empara de l'esprit de <span>Medivh</span> qui n'était encore qu'un embryon.
    Lors de ses 16 ans, cet esprit corrompu refit surfasse provoquant la mort de son père et un coma de 20 ans...
    <br /><br />
    À son réveil, <span>Medivh</span>, toujours corrompu par <span>Sargeras</span>, commenca à étudier la magie noire et à s'aventurer sous forme incorporelle le <span>Néant Distordu</span>, une dimension magique reliant tous les mondes et abritant un nombre infini de <span>Démons</span>.
    Un jour, pendant ses voyages dans le <span>Néant Distordu</span>, il parla au Démoniste <span>Gul'dan</span>.
    Il promit à ce dernier de lui révéler l'emplacement du <span>Tombeau de Sargeras</span> qui pourrait lui conferer un pouvoir divin, s'il déferlait la <span>Horde</span> en <span>Azeroth</span>.
    <span>Gul'dan</span> ayant accepté, <span>Medivh</span> ouvrit la <span>Porte des Ténèbres</span> pour permettre aux armées Orcs de venir en <span>Azeroth</span>.
    <br /><br />
    Mais, alors que la <span>Horde</span> marchait <span>Azeroth</span>, <span>Aegwynn</span> décida de mettre fin à la vie de son fils.
    Mais elle échoua et fut blessée.
    Cependant, peu de temps après, <span>Medvih</span> fut tué par son apprenti, <span>Khadgar</span> ainsi que le <span>Capitaine Lothar</span>.
    En détruisant le corps de <span>Medivh</span>, <span>Lothar</span> bannit l'esprit de <span>Sargeras</span>, ce qui permit à l'esprit de <span>Medivh</span> de continuer d'exister, purgé du contrôle du <span>Titan Noir</span>.
    <br /><br />
    Les années passèrent et <span>Medivh</span> fut ressuscité sous la forme d'un spectre.
    Grâce à lui, les Humains, les Orcs et les Elfes de la Nuit purent s'unir pour vaincre <span>Archimonde</span>, un des démons les plus puissants de la <span>Légion Ardente</span>, qui menaçait de détruire le monde.
    Ensemble, <span>Jaina Portvaillant</span>, <span>Thrall</span>, <span>Tyrande Murmevent</span> ( la grande prêtresse des Elfs de la nuit ), l'Archidruide <span>Malfurion Hurlorage</span> et <span>Medivh</span> avaient pu sauver <span>Azeroth</span>.
    <br /><br />
    Aujourd'hui, personne ne sait ce qu'il est advenu de l'esprit de <span>Medivh</span>.
    On peut voir des souvenirs de lui dans la tour de <span>Karazhan</span>, ou il a passé la plupart du temps à étudier.
    On peut aussi voir un autre souvenir de lui dans les <span>Grottes du Temps</span>, et le protéger au moment de l'ouverture de la <span>Porte des Ténèbres</span>.
