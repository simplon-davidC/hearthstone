    Autrefois, <span>Uther</span> était un chevalier et un clerc qui étudiait auprès d'<span>Alonsus Faol</span>, l'ancien évêque de <span>Comté-du-Nord</span>.
    Lors de la chute de <span>Hurlevent</span> par les Orcs de <span>Gul'dan</span>, <span>Alonsus</span> fonda l'<span>Ordre des Chevaliers de la Main d'argent</span>, composé de Chevaliers sacrés par la Lumière, qui furent appelés paladins.
    <span>Uther</span> était le premier d'entre eux.
    Les survivants de <span>Hurlevent</span> partirent en <span>Lordaeron</span> et combattirent pendant des années la <span>Horde</span> et remportèrent de nombreuses batailles.
    <br /><br />
<span>Uther</span> participa à la Bataille du <span>Mont Rochenoire</span> au côté de <span>Anduin Lothar</span> et du grand paladin <span>Turalyon</span>.
    Il fut d'ailleurs nommé par ce dernier le <span>Porteur de Lumière</span> et participa avec lui à l'assaut final de la <span>Porte des ténèbres</span> qui provoqua la disparition de <span>Turalyon</span>.
    Durant les années qui s'ensuivirent, <span>Uther</span> continua de servir l'<span>Alliance</span> et proposa au roi de <span>Lordaeron</span>, <span>Terenas Menethil</span>, de devenir le mentor de son fils, le <span>prince Arthas</span>.
    Il devint ensuite lui aussi un paladin de la <span>Main d'argent</span> et combattit avec <span>Uther</span> les Orcs et plus tard le <span>Fléau</span> mort-vivant.
    <br /><br />
    Ils combattirent à <span>Âtreval</span>, un petit village situé au Nord de <span>Lordaeron</span> pour tenter de secourir les citoyens des morts-vivants du <span>Fléau</span>.
    Hélas, la ville ne put être sauvée.
    Avec <span>Uther</span>, <span>Arthas</span> partit pour <span>Stratholme</span>, une grande ville de l'<span>Alliance</span> située à l'extrême Nord de <span>Lordaeron</span>, pour retrouver le responsable de la chute d'<span>Âtreval</span> : le démon <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/585">Mal’Ganis</a>
    <img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/585.jpg"></span>.
    Les deux paladins constatèrent que la ville était contaminée par la <span>Peste</span>.
    <span>Arthas</span> décida alors de tuer tous les citoyens de la ville.
    <span>Uther</span> refusa et fut retiré de ses fonctions par le prince de <span>Lordaeron</span>.
    <br /><br />
    Peu après qu'<span>Arthas</span> soit parti en <span>Norfendre</span> pour chasser <span class="over"><a class="rarete5" href="http://www.hearthstone-decks.com/carte/voir/585">Mal’Ganis</a>
    <img class="zecha-popover-image"  src="http://www.hearthstone-decks.com/upload/carte/585.jpg"></span>, <span>Uther</span> et <span>Terenas</span> décidèrent de le rappeler pour qu'il réponde de ses actes.
